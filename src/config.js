/**
* 验证11位手机号
* 联通号段 130 131 132 145 146 155 156 166 171 175 176 185 186
* 移动号段 134 135 136 137 138 139 147 148 150 151 152 157 158 159 172 178 182 183 184 187 188 198
* 电信号段 133 149 153 173 174 177 180 181 189 199
* 虚拟运营商 170
*/

/**
 * 中国移动
 * 13[4-9]
 * 14[7-8]
 * 15[0-27-9]
 * 17[28]
 * 18[3478]
 * 198
 * @type {RegExp}
 */
export const isCMCCExp = /^((\+86)?(13[4-9]|14[7-8]|15[0-27-9]|17[28]|18[3478]|198)\d{8})$/;
/**
 * 中国联通运营商
 * 13[0-2]
 * 14[5-6]
 * 15[5-6]
 * 166
 * 17[156]
 * 18[5-6]
 * @type {RegExp}
 */
export const isCUCCExp = /^((\+86)?(13[0-2]|14[5-6]|15[5-6]|166|17[156]|18[56])\d{8})$/;

/**
 * 中国电信
 * 133
 * 149
 * 153
 * 17[347]
 * 18[0189]
 * 199
 * @type {RegExp}
 */
export const isCTCCExp = /^((\+86)?(133|149|153|17[347]|18[0189]|199)\d{8})$/;

export const isVirtualExp = /^((\+86)?(170)\d{8})$/;
/**
 * 13[0-9]
 * 14[5-9]
 * 15[0-35-9]
 * 166
 * 17[0-8]
 * 18[0-9]
 * 19[8-9]
 * @type {RegExp}
 */
export const isChinaPhoneNoExp = /^((\+86)?(13\d|14[5-9]|15[0-35-9]|166|17[0-8]|18\d|19[8-9])\d{8})$/;

/**
 * 验证邮箱
 * @type {RegExp}
 */
export const isEmailExp = /^[A-Za-z0-9]+[A-Za-z0-9\-_.]*@([A-Za-z0-9]+\.)+[A-Za-z0-9]+$/;

/**
 * 中国身份证
 * 15位数字
 * 17位数字加一位数字
 * 17位数字加X或者x
 * @type {RegExp}
 */
export const identityCardNumberExp = /^((\d{15})|((\d{17})(\d|X|x)))$/;
